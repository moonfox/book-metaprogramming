# =============================
# 目标1
# 1. 顶层上下文的self是?
# 2. 这个self的类是？
# =============================
# 结论
# 1. 顶层上下文的self是main对象
# 2. main对象的class是Object
# =============================
lambda {
  puts self # => main
  puts self.class # => Object
}

